function initWorldMap(dataProvider) {
    window.mapZoom = 0;

    window.ndcMap = AmCharts.makeChart("mapdiv", {
        type: "map",
        areasSettings: {
            "autoZoom": true,
            "color": "#e6e6e6",
            "outlineColor": "#c3c3c3",
            "rollOverOutlineColor": "#c3c3c3",
            "selectedColor": "#1b40ff",
            "unlistedAreasAlpha": 0,
        },
        dataProvider: dataProvider,
        export: {
            enabled: true
        }
    });

    function handleGoHome() {
        window.mapZoom = 0;
        jgn("#ndc-country-player-details").hide();
        reInitMap(worldDataProvider);
    }

    function reInitMap(newDataProvider) {
        jgn("#mapdiv").empty();
        initWorldMap(newDataProvider);
    }

    function handleMapObjectClick(event) {
        if (event.mapObject.id == "backButton") {
            handleGoHome();
        }
        else {
            if (window.mapZoom == 1) {
                showCountryDetails(event.mapObject.title);
            }
            else {
                var found = false;
                if (!found) {
                    jgn(window.europeAreas).each(function (index, object) {
                        if (object.id == event.mapObject.id) {
                            found = true;
                            reInitMap(europeProvider);
                        }
                    })
                }
                if (!found) {
                    jgn(window.africaMiddleEastAreas).each(function (index, object) {
                        if (object.id == event.mapObject.id) {
                            found = true;
                            reInitMap(africaMiddleEastProvider);
                        }
                    })
                }
                if (!found) {
                    jgn(window.asiaPacificAreas).each(function (index, object) {
                        if (object.id == event.mapObject.id) {
                            found = true;
                            reInitMap(asiaPacificProvider);
                        }

                    })
                }
                if (!found) {
                    jgn(window.northAsiaAreas).each(function (index, object) {
                        if (object.id == event.mapObject.id) {
                            found = true;
                            reInitMap(northAsiaProvider);
                        }
                    })
                }
                if (!found) {
                    jgn(window.americaAreas).each(function (index, object) {
                        if (object.id == event.mapObject.id) {
                            found = true;
                            reInitMap(americaProvider);
                        }
                    })
                }

                window.mapZoom = 1;
            }
        }
    }

    // monitor when home icon was clicked and also go to continents map
    window.ndcMap.addListener("homeButtonClicked", handleGoHome);
    window.ndcMap.addListener("clickMapObject", handleMapObjectClick);
}

function showCountryDetails(country) {
    jgn("#ndc-country-player-details").hide();

    window.ailinesCountOnMap = 0;
    window.aggregatorsCountOnMap = 0;
    window.sellersCountOnMap = 0;
    jgn(NDCProfiles).each(function (index, player) {
        if (player.country == country) {
            switch (player.type) {
                case "Airline":
                    window.ailinesCountOnMap++;
                    break;
                case "Seller":
                    window.sellersCountOnMap++;
                    break;
                case "Aggregator":
                    window.aggregatorsCountOnMap++;
                    break;
            }
        }
    });
    jgn("#ndc-players-aggregators-count-country").html(window.aggregatorsCountOnMap);
    jgn(".ndc-aggregators-count").attr("title", window.aggregatorsCountOnMap + " aggregators");
    jgn("#ndc-players-sellers-count-country").html(window.sellersCountOnMap);
    jgn(".ndc-sellers-count").attr("title", window.sellersCountOnMap + " sellers");
    jgn("#ndc-players-airlines-count-country").html(window.ailinesCountOnMap);
    jgn(".ndc-airlines-count").attr("title", window.ailinesCountOnMap + " airlines");

    window.selectedCountryForAdvancedSearch = country;

    jgn("#ndc-country-player-details").delay(1000).show(0);
}
